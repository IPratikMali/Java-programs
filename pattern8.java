
public class pattern8 {

    public static void main(String args[]) {
        for (int j = 5; j > 0; j--) {
            for (int i = 0; i < 5-j; i++) {
                int d = 1;
                System.out.print(d);
                d++;
            }
            System.out.println();
        }
    }
}


